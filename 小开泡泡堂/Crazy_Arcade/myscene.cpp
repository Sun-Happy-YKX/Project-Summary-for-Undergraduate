#include "myscene.h"

myscene::myscene(QObject *parent) : QGraphicsScene(parent)
{
    for(int i=0;i<20;i++){
        for(int j=0;j<20;j++){
             item[i][j]=new myitem("grass");
            item[i][j]->setPos(i*item[i][j]->boundingRect().height(),j*item[i][j]->boundingRect().height());
           item[i][j]->fire= item[i][j]->bomb=item[i][j]->wall=false;
            this->addItem(item[i][j]);
        }
    }
}
void myscene::setProps(){
   this->shoes=0;
   int X[200],Y[200];
   int count=0,num=0;
   for(int i=0;i<20;i++)
       for(int j=0;j<20;j++)
       {
           if(this->isdestroy(i,j)&&this->iswall(i,j)){
               X[count]=i;
               Y[count]=j;
               count++;
           }
       }
   for(int i=0;i<10;i++)
           {item[X[i]][Y[i]]->setProp();
            int NUM=num%13;
            this->item[X[i]][Y[i]]->setPropssname(NUM);
            num++;
            item[X[i+count/3]][Y[i+count/3]]->setProp();
            NUM=num%13;
            if(NUM>=3&&NUM<6)
               {this->shoes++;
                if(this->shoes>2)
                   NUM=NUM+5;}
            this->item[X[i+count/3]][Y[i+count/3]]->setPropssname(NUM);
            num++;
            item[X[i+2*count/3]][Y[i+2*count/3]]->setProp();
            NUM=num%13;
            if(NUM>=3&&NUM<6)
            {this->shoes++;
             if(this->shoes>2)
                NUM=NUM-3;}
            this->item[X[i+2*count/3]][Y[i+2*count/3]]->setPropssname(NUM);
            num++;
       }
}
